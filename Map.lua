map.Load = function()
  print("Map : Chargement des textures...")
  
  map.TileSheet = love.graphics.newImage("Images/Map/Orcs_TileSheetMap.png")
  local nbColumns = map.TileSheet:getWidth() / TILE_WIDTH
  local nbLines = map.TileSheet:getHeight() / TILE_HEIGHT
  local id = 1
  
  map.TileTextures[0] = nil

  for l=1,nbLines do
    for c=1,nbColumns do
      map.TileTextures[id] = love.graphics.newQuad(
        (c-1)*TILE_WIDTH, (l-1)*TILE_HEIGHT,
        TILE_WIDTH, TILE_HEIGHT, 
        map.TileSheet:getWidth(), map.TileSheet:getHeight()
        )
      id = id + 1
    end
  end
  
  print("Map :Chargement des textures terminÃ©es...")
end

--Dessin de la carte
map.Draw = function()
  
  for l=1,MAP_HEIGHT do
    for c=1,MAP_WIDTH do
      local id = map.Map[l][c]
      local texQuad = map.TileTextures[id]
      if texQuad ~= nil then
        love.graphics.draw(map.TileSheet, texQuad, (c-1)*TILE_WIDTH, (l-1)*TILE_HEIGHT)
      end
    end
  end
  
  local x = love.mouse.getX()
  local y = love.mouse.getY()
  local c = math.floor(x / TILE_WIDTH) + 1
  local l = math.floor(y / TILE_HEIGHT) + 1
  if l>0 and c>0 and l <= MAP_HEIGHT and c <= MAP_WIDTH then
    local id = map.Map[l][c]
  end
end

